/**
 * Script to fetch all image URLs from Cloudinary
 * Run: node --env-file=.env scripts/fetch-cloudinary-urls.js
 * 
 * Reads credentials from .env file
 */

import https from 'https';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const CLOUD_NAME = process.env.CLOUDINARY_CLOUD_NAME || 'dbmetyesk';
const API_KEY = process.env.CLOUDINARY_API_KEY;
const API_SECRET = process.env.CLOUDINARY_API_SECRET;

if (!API_KEY || !API_SECRET) {
    console.error('Please set CLOUDINARY_API_KEY and CLOUDINARY_API_SECRET environment variables');
    console.log('\nExample:');
    console.log('CLOUDINARY_API_KEY=your_key CLOUDINARY_API_SECRET=your_secret node scripts/fetch-cloudinary-urls.js');
    process.exit(1);
}

const auth = Buffer.from(`${API_KEY}:${API_SECRET}`).toString('base64');

function fetchResources(nextCursor = null) {
    return new Promise((resolve, reject) => {
        let url = `https://api.cloudinary.com/v1_1/${CLOUD_NAME}/resources/image?max_results=500`;
        if (nextCursor) {
            url += `&next_cursor=${nextCursor}`;
        }

        const urlObj = new URL(url);

        const options = {
            hostname: urlObj.hostname,
            path: urlObj.pathname + urlObj.search,
            method: 'GET',
            headers: {
                'Authorization': `Basic ${auth}`,
            },
        };

        const req = https.request(options, (res) => {
            let data = '';
            res.on('data', (chunk) => data += chunk);
            res.on('end', () => {
                try {
                    resolve(JSON.parse(data));
                } catch (e) {
                    reject(e);
                }
            });
        });

        req.on('error', reject);
        req.end();
    });
}

async function main() {
    console.log(' Fetching images from Cloudinary...');

    let allResources = [];
    let nextCursor = null;

    do {
        const response = await fetchResources(nextCursor);

        if (response.error) {
            console.error('Error:', response.error.message);
            process.exit(1);
        }

        allResources = allResources.concat(response.resources || []);
        nextCursor = response.next_cursor;
        console.log(` Fetched ${allResources.length} images...`);
    } while (nextCursor);

    // Filter and sort earth sequence images (those with frame numbers)
    const earthImages = allResources
        .filter(r => /^\d{5}/.test(r.public_id.split('/').pop()))
        .sort((a, b) => {
            const numA = parseInt(a.public_id.match(/\d{5}/)?.[0] || '0');
            const numB = parseInt(b.public_id.match(/\d{5}/)?.[0] || '0');
            return numA - numB;
        });

    console.log(`\n Found ${earthImages.length} earth sequence images`);

    // Generate URLs array
    const urls = earthImages.map(r => r.secure_url);

    // Save to config file
    const configContent = `// Auto-generated by scripts/fetch-cloudinary-urls.js
// Total frames: ${urls.length}

export const EARTH_FRAME_URLS: string[] = [
${urls.map(url => `  '${url}',`).join('\n')}
];

export const TOTAL_FRAMES = ${urls.length};
`;

    const outputPath = path.join(__dirname, '../src/config/cloudinaryUrls.ts');
    const outputDir = path.dirname(outputPath);

    if (!fs.existsSync(outputDir)) {
        fs.mkdirSync(outputDir, { recursive: true });
    }

    fs.writeFileSync(outputPath, configContent);
    console.log(`\n Saved to: ${outputPath}`);
    console.log(` Total frames: ${urls.length}`);
}

main().catch(console.error);
